package skyline

import (
	"reflect"
	"testing"
)

func TestGetSkyline(t *testing.T) {
	var tests = []struct {
		buildings [][]int
		want      [][]int
	}{
		{[][]int{{2, 9, 10}}, [][]int{{2, 10}, {9, 0}}},
		{[][]int{{2, 9, 10}, {3, 7, 15}}, [][]int{{2, 10}, {3, 15}, {9, 0}}},
		{[][]int{{2, 9, 10}, {3, 7, 8}}, [][]int{{2, 10}, {9, 0}}},
		{[][]int{{2, 9, 10}, {3, 11, 8}}, [][]int{{2, 10}, {9, 8}, {11, 0}}},
		{[][]int{{2, 9, 10}, {3, 11, 15}}, [][]int{{2, 10}, {3, 15}, {11, 0}}},
		{[][]int{{2, 9, 10}, {9, 12, 10}}, [][]int{{2, 10}, {12, 0}}},
		{[][]int{{2, 9, 10}, {9, 12, 12}}, [][]int{{2, 10}, {9, 12}, {12, 0}}},
		{[][]int{{2, 9, 10}, {9, 12, 8}}, [][]int{{2, 10}, {9, 8}, {12, 0}}},
		{[][]int{{2, 9, 10}, {11, 13, 12}}, [][]int{{2, 10}, {9, 0}, {11, 12}, {13, 0}}},
		{[][]int{{2, 9, 10}, {3, 7, 15}, {6, 13, 12}}, [][]int{{2, 10}, {3, 15}, {7, 12}, {13, 0}}},
		{[][]int{{2, 9, 10}, {3, 7, 15}, {4, 6, 20}}, [][]int{{2, 10}, {3, 15}, {4, 20}, {9, 0}}},
		{[][]int{{2, 9, 10}, {3, 7, 15}, {4, 8, 18}}, [][]int{{2, 10}, {3, 15}, {4, 18}, {9, 0}}},
		{[][]int{{2, 9, 10}, {3, 7, 15}, {4, 12, 18}}, [][]int{{2, 10}, {3, 15}, {4, 18}, {12, 0}}},
		{[][]int{{2, 9, 10}, {3, 7, 15}, {7, 12, 15}}, [][]int{{2, 10}, {3, 15}, {12, 0}}},
		{[][]int{{2, 9, 10}, {3, 7, 15}, {9, 12, 10}}, [][]int{{2, 10}, {3, 15}, {12, 0}}},
		{[][]int{{2, 9, 10}, {3, 7, 15}, {11, 12, 17}}, [][]int{{2, 10}, {3, 15}, {9, 0}, {11, 17}, {12, 0}}},
		{[][]int{{2, 9, 10}, {3, 7, 15}, {7, 9, 15}}, [][]int{{2, 10}, {3, 15}, {9, 0}}},
		{[][]int{{2, 9, 10}, {3, 7, 8}, {7, 8, 9}}, [][]int{{2, 10}, {9, 0}}},
		{[][]int{{2, 9, 10}, {3, 7, 8}, {9, 12, 10}}, [][]int{{2, 10}, {12, 0}}},
		{[][]int{{2, 9, 10}, {3, 7, 8}, {3, 7, 15}}, [][]int{{2, 10}, {3, 15}, {9, 0}}},
		{[][]int{{2, 9, 10}, {3, 7, 15}, {5, 12, 12}, {15, 20, 10}, {19, 24, 8}}, [][]int{{2, 10}, {3, 15}, {7, 12}, {12, 0}, {15, 10}, {20, 8}, {24, 0}}},
		{[][]int{{0, 2, 3}, {2, 5, 3}}, [][]int{{0, 3}, {5, 0}}},
		{[][]int{{1, 2, 1}, {1, 2, 2}, {1, 2, 3}}, [][]int{{1, 3}, {2, 0}}},
	}
	for _, test := range tests[1:] {
		if got := getSkyline(test.buildings); !reflect.DeepEqual(got, test.want) {
			t.Errorf("getSkyline(%v) = %v, want %v", test.buildings, got, test.want)
		}
	}
}
